package com.bc.d3.data;

public class Item {

	private String id;
	private String name;
	private String icon;
	private String displayColor;
	private String tooltipParams;

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getIcon() {
		return icon;
	}

	public String getDisplayColor() {
		return displayColor;
	}

	public String getTooltipParams() {
		return tooltipParams;
	}

}
